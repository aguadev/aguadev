CREATE TABLE  flowcell (
  flowcell_id int(10) unsigned NOT NULL AUTO_INCREMENT,
  server varchar(150) NOT NULL,
  flowcell_barcode varchar(45) NOT NULL,
  update_timestamp timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  fpga_version varchar(100) NOT NULL,
  rta_version varchar(100) NOT NULL,
  run_length varchar(100) NOT NULL,
  indexed enum('no','yes') NOT NULL DEFAULT 'no',
  status_id int(10) unsigned NOT NULL,
  operator varchar(100) DEFAULT NULL,
  description varchar(250) DEFAULT NULL,
  recipe varchar(250) DEFAULT NULL,
  machine_name varchar(45) NOT NULL,
  location varchar(250) NOT NULL,
  run_start_date date NOT NULL,
  priority_flowcell int(2) NOT NULL DEFAULT '0',
  location_md5sum varchar(45) NOT NULL,
  comments text,
  fail_code_id int(10) DEFAULT NULL,
  user_code_and_ip text,
  attempting_rehyb varchar(1) DEFAULT NULL,
  lcm_broad_cause int(10) DEFAULT NULL,
  lcm_specific_cause int(10) DEFAULT NULL,
  lcm_status varchar(45) DEFAULT NULL,
  lcm_equipment_related varchar(45) DEFAULT NULL,
  lcm_comments text,
  PRIMARY KEY (flowcell_id),
  UNIQUE KEY location_md5sum_un (location_md5sum),
  KEY fc_loc (location),
  KEY flowcell_barcode (flowcell_barcode),
  KEY status_id (status_id)
)

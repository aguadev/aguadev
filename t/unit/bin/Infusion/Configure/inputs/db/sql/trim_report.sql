CREATE TABLE  trim_report (
  trim_report_id int(10) unsigned NOT NULL AUTO_INCREMENT,
  flowcell_id int(10) unsigned NOT NULL,
  lane int(2) NOT NULL,
  run_folder varchar(250) NOT NULL,
  qscore_cycle int(5) NOT NULL,
  report_time varchar(45) DEFAULT NULL,
  per_good_tiles decimal(5,2) DEFAULT NULL,
  num_good_tiles int(11) DEFAULT NULL,
  total_tiles int(11) DEFAULT NULL,
  pass_yield_gb decimal(5,2) DEFAULT NULL,
  total_yield_gb decimal(5,2) DEFAULT NULL,
  pass_per_q30 decimal(5,2) DEFAULT NULL,
  total_per_q30 decimal(5,2) DEFAULT NULL,
  pass_mean_qscore decimal(5,2) DEFAULT NULL,
  total_mean_qscore decimal(5,2) DEFAULT NULL,
  tile_fail_reasons text,
  good_tiles text,
  bad_tiles text,
  check_md5sum varchar(200) NOT NULL,
  PRIMARY KEY (trim_report_id),
  UNIQUE KEY un_fc_lane (flowcell_id,lane),
  KEY lane_idx (lane),
  KEY fc_id_idx (flowcell_id)
)

CREATE TABLE  sample_history (
  sample_history_id int(10) unsigned NOT NULL AUTO_INCREMENT,
  sample_id int(10) unsigned NOT NULL,
  project_id int(10) unsigned NOT NULL,
  sample_barcode varchar(55) NOT NULL,
  sample_name varchar(100) DEFAULT NULL,
  plate_barcode varchar(45) DEFAULT NULL,
  species varchar(50) DEFAULT NULL,
  gender enum('M','F','U') DEFAULT NULL,
  volume varchar(10) DEFAULT NULL,
  concentration varchar(20) DEFAULT NULL,
  od_260_280 varchar(10) DEFAULT NULL,
  tissue_source varchar(45) DEFAULT NULL,
  extraction_method varchar(45) DEFAULT NULL,
  ethnicity varchar(50) DEFAULT NULL,
  parent_1_sample_id int(10) unsigned DEFAULT NULL,
  parent_2_sample_id int(10) unsigned DEFAULT NULL,
  replicates_id int(10) unsigned DEFAULT NULL,
  cancer enum('N','Y') DEFAULT 'N',
  match_sample_ids int(10) unsigned DEFAULT NULL,
  match_sample_type varchar(45) DEFAULT NULL,
  comment varchar(100) DEFAULT NULL,
  due_date date DEFAULT NULL,
  target_fold_coverage varchar(45) DEFAULT NULL,
  gt_gender enum('M','F') DEFAULT NULL,
  do_build enum('no','yes') NOT NULL DEFAULT 'yes',
  status_id int(10) NOT NULL,
  sample_policy text,
  update_date timestamp NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  delivered_date date DEFAULT NULL,
  genotype_report text,
  gt_deliv_src text,
  user_code_and_ip varchar(255) DEFAULT NULL,
  PRIMARY KEY (sample_history_id)
)
